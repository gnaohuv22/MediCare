package Models;

/**
 *
 * @author hoang
 */
public class Relationship {
    private String id, relation;

    public Relationship() {
    }

    public Relationship(String id, String relation) {
        this.id = id;
        this.relation = relation;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRelation() {
        return relation;
    }

    public void setRelation(String relation) {
        this.relation = relation;
    }
    
    
}
