package Models;

/**
 *
 * @author hoang
 */
public class Breadcrumb {
    private String url, name;

    public Breadcrumb() {
    }

    public Breadcrumb(String url, String name) {
        this.url = url;
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
