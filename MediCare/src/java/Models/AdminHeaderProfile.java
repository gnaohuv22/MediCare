package Models;

/**
 *
 * @author DELL
 */
public class AdminHeaderProfile {
    private String name;
    private String link;

    public AdminHeaderProfile() {
    }

    public AdminHeaderProfile(String name, String link) {
        this.name = name;
        this.link = link;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
    
}
