package Models;

/**
 *
 * @author hoang
 */
public class SocialNetworks {
    private String id, socialName, icon, link;

    public SocialNetworks() {
    }

    public SocialNetworks(String id, String socialName, String icon, String link) {
        this.id = id;
        this.socialName = socialName;
        this.icon = icon;
        this.link = link;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSocialName() {
        return socialName;
    }

    public void setSocialName(String socialName) {
        this.socialName = socialName;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
    
    
}
